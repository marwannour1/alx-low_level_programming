#include <stdio.h>

/**
 * main - Entry point
 *
 * Description: prints sizes of data types
 *
 * Return: Always 0 (Success)
 */
int main(void)
{
	printf("Size of char: %d byte(s)\n", sizeof(char));
	printf("Size of int: %d byte(s)\n", sizeof(char));
	printf("Size of long int: %d byte(s)\n", sizeof(char));
	printf("Size of long long int: %d byte(s)\n", sizeof(char));
	printf("Size of float: %d byte(s)\n", sizeof(char));
	return (0);
}
